#Integer
umur = 18 
print ("Umur:", umur)

#Tipe Data Float
tinggi = 173
print ("Tinggi:", tinggi)

#String
nama = "Ghulam"
print("Nama:", nama)

#Boolean
is_student = True
print ("Is Student:", is_student)

#List
buah = ["apel", "Nangka", "Durian"]
print("Buah:", buah)

#Tuple
koordinat = (10, 20)
print("Koordinat:", koordinat)

#Dictionary
data_mahasiswa = {"nama": "Ghulam", "umur": 18, "is_student": True}
print("Data Mahasiswa:", data_mahasiswa)

# Set
angka_unik = {1, 2, 3, 4, 5}
print("Angka Unik:", angka_unik)

